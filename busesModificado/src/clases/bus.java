    private void t_busesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_busesMouseClicked
        int column = t_buses.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/t_buses.getRowHeight();
        if(row < t_buses.getRowCount() && row >= 0 && column < t_buses.getColumnCount() && column >= 0){
            Object value = t_buses.getValueAt(row, column);
            if(value instanceof JButton){
                ((JButton)value).doClick();
                JButton boton = (JButton) value; 
                if(boton.getName().equals("h")){
                    JOptionPane.showMessageDialog(null, "Viendo Horarios");
                }
                if(boton.getName().equals("ed")){
                    JOptionPane.showMessageDialog(null, "Editando");
                } 
            }
        }
    }//GEN-LAST:event_t_busesMouseClicked

