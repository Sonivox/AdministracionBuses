    public  void mostrarbuses(String buscar) throws SQLException{
        String [] titulos={"TIPO","BUS","VIAJES","ACTUALIZACION", "ACCIONES"};
        String [] w = new String[5];
        String sql = "SELECT * FROM buses WHERE LOWER(nombre) LIKE LOWER('%"+buscar+"%') AND TIPO IS NOT NULL";
         modelo1 = new DefaultTableModel(null,titulos);
        
         try {
              Statement st = con.createStatement();
              ResultSet rs = st.executeQuery(sql);
              while (rs.next()){
                  w[0]=rs.getString("tipo");
                  w[1]=rs.getString("nombre");
                  w[2]=rs.getString("viajes");
                  w[3]=rs.getString("parada");
//                  w[4]=rs.getString("precio");
                  modelo1.addRow(w);
              }
              t_buses.setModel(modelo1);
              
         } catch (SQLException e) {
             JOptionPane.showMessageDialog(null, e);
         } 
    }
